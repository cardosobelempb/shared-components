export function computePaddingClasses(obj?: DF_PADDING_OBJECT) {
  if (!obj) return null;
  return [
    "obj",
    obj.p && DF_PADDING_PROPS.p[obj.p],
    obj.pb && DF_PADDING_PROPS.pb[obj.pb],
    obj.pe && DF_PADDING_PROPS.pe[obj.pe],
    obj.pl && DF_PADDING_PROPS.pl[obj.pl],
    obj.pr && DF_PADDING_PROPS.pr[obj.pr],
    obj.ps && DF_PADDING_PROPS.ps[obj.ps],
    obj.pt && DF_PADDING_PROPS.pt[obj.pt],
    obj.px && DF_PADDING_PROPS.px[obj.px],
    obj.py && DF_PADDING_PROPS.py[obj.py],
  ]
    .filter(Boolean)
    .join(" ");
}
